//----------------------------------------------------------------------------------------------
// AudioSystemUI.h
// Brief: Uses AudioSystem functions and creates UI for each fuction that the user can use. 
// The UI includes float sliders, buttons and input text boxes.
// Edited By: Jeremy Zoitas.
//----------------------------------------------------------------------------------------------
#pragma once
#include <imgui.h>
#include "AudioSystem.h"

class AudioSystemUI
{
public:
	//----------------------------------------------------------------------------------------------
	// Default constructor.
	//----------------------------------------------------------------------------------------------
	AudioSystemUI();

	//----------------------------------------------------------------------------------------------
	// Default destructor.
	//----------------------------------------------------------------------------------------------
	~AudioSystemUI();

	//----------------------------------------------------------------------------------------------
	// Updates and creates the UI being used, it also calls some functions from audio system, such
	// as update, and frequency analysis.
	//----------------------------------------------------------------------------------------------
	void Update();

	//----------------------------------------------------------------------------------------------
	// A public array of floats which holds spectrum data spread out over a range of 2048.
	//----------------------------------------------------------------------------------------------
	float m_fSpectrumData[2048];

protected:
	//----------------------------------------------------------------------------------------------
	// A pointer to Audio System.
	//----------------------------------------------------------------------------------------------
	AudioSystem * m_pAudioSystem;

	//----------------------------------------------------------------------------------------------
	// A bool which is used to pause the audio.
	//----------------------------------------------------------------------------------------------
	bool m_bPause;

	//----------------------------------------------------------------------------------------------
	// A bool which is used to determine if the audio is playing.
	//----------------------------------------------------------------------------------------------
	bool m_bPlaying;

	//----------------------------------------------------------------------------------------------
	// A float used for the intensity of the data.
	//----------------------------------------------------------------------------------------------
	float m_fIntensity;

	//----------------------------------------------------------------------------------------------
	// A float used for changing the volume. 
	//----------------------------------------------------------------------------------------------
	float m_fVolume;

	//----------------------------------------------------------------------------------------------
	// A char pointer used for loading audio. 
	//----------------------------------------------------------------------------------------------
	char* m_chBuf;

	//----------------------------------------------------------------------------------------------
	// A char pointer used for loading audio. 
	//----------------------------------------------------------------------------------------------
	const char* m_chFileDir;
	
};

